    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int rowIndex= categoryTable.getSelectedRow();
        if(rowIndex!=-1){
            int result=JOptionPane.showConfirmDialog(null,"Do you really want to delete?","Deletion Confirmation",JOptionPane.YES_NO_OPTION);
            if(result==JOptionPane.YES_OPTION){
                String categoryValue=(String)categoryTable.getValueAt(rowIndex, 1);
                try{
                    Database.db.st.executeUpdate("delete from categorytable where category='"+categoryValue+"'");
                    JOptionPane.showMessageDialog(null,"Category "+categoryValue+" has been deleted successfully");
                    getEntries(); 
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
            }  
        }   
    }//GEN-LAST:event_deleteButtonActionPerformed

    

