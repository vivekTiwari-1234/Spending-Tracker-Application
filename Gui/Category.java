    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String categoryValue=categoryText.getText();
            if(!categoryValue.equals("")){
                Database.db.st.executeUpdate("insert into categorytable values('"+categoryValue+"')");
                JOptionPane.showMessageDialog(null,"Category "+categoryValue+" added successfully");
                getEntries();
                categoryText.setText("");
            }else{
                JOptionPane.showMessageDialog(null,"Please specify any value for this");
            }
        }catch(SQLIntegrityConstraintViolationException ex){
            JOptionPane.showMessageDialog(null,"Already exists!!!");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

