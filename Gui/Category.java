    private void getEntries(){
       try{
          javax.swing.table.DefaultTableModel dtm= (javax.swing.table.DefaultTableModel)categoryTable.getModel();
          int rowCount=dtm.getRowCount();
          while(rowCount--!=0){
              dtm.removeRow(0);
          }
          ResultSet rs= Database.db.st.executeQuery("select* from categorytable");
          int sno=0;
          while(rs.next()){
              String categoryValue=rs.getString("category");
//              javax.swing.table.DefaultTableModel dtm= (javax.swing.table.DefaultTableModel)categoryTable.getModel();;
//              Object obj[]={++sno,categoryValue};
//              dtm.addRow(obj);
                Vector v= new Vector();
                v.add(++sno);
                v.add(categoryValue);
                dtm.addRow(v);     
          }
       }catch(Exception e){
           JOptionPane.showMessageDialog(null,e);
       }
    }

