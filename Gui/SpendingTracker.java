    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            java.util.Date d=jDateChooser1.getDate();
            String amount=AmountField.getText();
            String categoryValue= (String)CategoryDropDown.getSelectedItem();
            if(d.equals("") && !amount.equals("") && !categoryValue.equals("")){
                int amount1 = Integer.parseInt(amount);
                java.sql.Date sqldate1=new java.sql.Date(d.getTime());
                Database.db.st.executeUpdate("insert into spending(sdate,category,amount) values('"+sqldate1+"','"+categoryValue+"','"+amount1+"')");
                JOptionPane.showMessageDialog(null,"Expense added successfully!");
            }
            else{
                JOptionPane.showMessageDialog(null,"Please specify all the values to store your spending");
            }
            getSpedingEntries();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void RemoveSpendingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveSpendingActionPerformed
        int rowIndex = SpendingTable.getSelectedRow();
        if(rowIndex!=-1){
            int result = JOptionPane.showConfirmDialog(null,"Do you really want to delete this spending entry ?","Remove Spending Confirmation"
                ,JOptionPane.YES_NO_CANCEL_OPTION);
            if(result==JOptionPane.YES_OPTION){        
                int sidValue= (int)SpendingTable.getValueAt(rowIndex,0);
                try{
                    Database.db.st.executeUpdate("DELETE from spending where sid='"+sidValue+"'");
                    JOptionPane.showMessageDialog(null,"Spending entry has been successfully deleted!!!");
                    getSpedingEntries();
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null,e);
                }
            }
        
        }     
    }//GEN-LAST:event_RemoveSpendingActionPerformed

