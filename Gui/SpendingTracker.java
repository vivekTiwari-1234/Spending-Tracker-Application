    private void getSpedingEntries(){
        try{
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)SpendingTable.getModel();
            int rowCount= dtm.getRowCount();
            while(rowCount--!=0){
                dtm.removeRow(0);
            }
            java.time.LocalDate currentDate=java.time.LocalDate.now();
            java.time.LocalDate beforedate=currentDate.minusDays(30);
            ResultSet rs=Database.db.st.executeQuery("select* from spending where sdate<='"+currentDate+"' and sdate>='"+beforedate+"'");
            int totalAmount=0;
            while(rs.next()){
                totalAmount+=rs.getInt("amount");
                Vector v= new Vector();
                v.add(rs.getInt("sid"));
                v.add(rs.getString("category"));
                v.add(rs.getDate("sdate"));
                v.add(rs.getInt("amount"));
                dtm.addRow(v);
            }
            TableAmount.setText(totalAmount+"");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
